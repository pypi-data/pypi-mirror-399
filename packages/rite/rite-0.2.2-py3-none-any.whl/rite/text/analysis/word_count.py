"""
Word Counting
=============

Count words in text.

"""


# =============================================================================
# Imports
# =============================================================================

# Import | Future
from __future__ import annotations

# =============================================================================
# Functions
# =============================================================================


def word_count(text: str) -> int:
    """
    Count the number of words in text.

    Args:
        text: Input text string

    Returns:
        Number of words

    Example:
        >>> word_count("hello world")
        2
    """
    if not text or not text.strip():
        return 0
    return len(text.split())


