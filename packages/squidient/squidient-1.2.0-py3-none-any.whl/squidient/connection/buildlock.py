from ..utils.log import *

logger = logging.getLogger(logging_context)


class BuildLock:

    def __init__(self):
        self._locks = {}

    def get_lock(self, key):
        if key in self._locks:
            return self._locks[key]
        else:
            return -1

    def set_lock(self, key, value):
        self._locks[key] = value
