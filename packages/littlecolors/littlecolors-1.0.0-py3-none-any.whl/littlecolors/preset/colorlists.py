# Imports ----------------------------------------------------------------------
from enum import Enum
from littlecolors import Color


# Saved Colors Lists -----------------------------------------------------------
class CLISTS(Enum):
    """
    Namespace for preset Color lists.

    Examples
    --------
    >>> from littlecolors.preset import CLISTS
    >>> colors_list = CLISTS.THREE()
    """

    def __call__(self) -> list[Color]:
        return [Color(c) for c in self.value]

    # My three favorite colors to use in plots together
    THREE1 = [
        (  0,  95, 115),
        (238, 155,   0),
        (222,  31, 107),
    ]

